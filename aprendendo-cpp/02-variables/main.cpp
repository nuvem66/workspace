#include <iostream>

int main() {

    int x; // Declaration, integer
    x = 5; // Assignment
    std::cout << x << std::endl;

    int y = 10; 
    // Combining the two steps into one!
    std::cout << y << std::endl;
    
    int sum = x + y;
    std::cout << sum << std::endl;
    
    return 0;
}